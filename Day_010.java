package Increment_10;

public class Main {
    
    public static void main(String[] args) {
        int a, b, c, d;
        
        // Increment Post => (a = a + 1)
        a = 1;
        a++; // ++ artinya tambah 1
        System.out.println(a);
        
        // Increment pree => (b = 1 + b)
        b = 2;
        ++b; 
        System.out.println(b);
        
        // Decrement Post => (c = c - 1)
        c = 3;
        c--; // -- artinya kurang 1
        System.out.println(c);
        
        // Decrement Pree => (d = 1 - d)
        d = 4;
        --d;
        System.out.println(d);
        
    }
    
}
