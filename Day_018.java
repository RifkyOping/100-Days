package Loop;

public class Main {
    
    public static void main(String[] args) {
        /*
        Perulangan For : Merupakan perulangan counted loop, yakni
                         perulangan yang tentu dan terhitung atau memiliki
                         jumlah yang pasti.
        */
        for(int a = 0; a <= 50; a++){
            System.out.print(a+" ");
        }
        
    }
    
}
